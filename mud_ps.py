import socket
import sys
import os
import threading
from datetime import datetime

# ── دعم النص العربي ──────────────────────────
def ar(text):
    """دالة لطباعة النص العربي بشكل صحيح في الطرفية"""
    try:
        print(text)
    except UnicodeEncodeError:
        print(text.encode('utf-8', errors='replace').decode('ascii', errors='replace'))

# ── الألوان ───────────────────────────────────
class لون:
    أخضر   = '\033[92m'
    أحمر   = '\033[91m'
    أصفر   = '\033[93m'
    أزرق   = '\033[94m'
    سماوي  = '\033[96m'
    عريض   = '\033[1m'
    إعادة  = '\033[0m'

# ── البانر ────────────────────────────────────
def banner():
    os.system('clear' if os.name == 'posix' else 'cls')
    ar(f"""
{لون.سماوي}{لون.عريض}
╔══════════════════════════════════════╗
║        🔍 أداة مسح المنافذ           ║
║         Port Scanner v1.0            ║
║      مدارك AI Tools — بالعربية       ║
╚══════════════════════════════════════╝
{لون.إعادة}""")

# ── قائمة المنافذ الشائعة ─────────────────────
منافذ_شائعة = {
    21:   "FTP",
    22:   "SSH",
    23:   "Telnet",
    25:   "SMTP",
    53:   "DNS",
    80:   "HTTP",
    110:  "POP3",
    143:  "IMAP",
    443:  "HTTPS",
    445:  "SMB",
    3306: "MySQL",
    3389: "RDP",
    5432: "PostgreSQL",
    6379: "Redis",
    8080: "HTTP-Alt",
    8443: "HTTPS-Alt",
    27017:"MongoDB",
}

# ── نتائج المسح ───────────────────────────────
منافذ_مفتوحة = []
قفل = threading.Lock()

# ── فحص منفذ واحد ────────────────────────────
def فحص_منفذ(هدف, منفذ, مهلة):
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        s.settimeout(مهلة)
        نتيجة = s.connect_ex((هدف, منفذ))
        s.close()
        if نتيجة == 0:
            خدمة = منافذ_شائعة.get(منفذ, "غير معروف")
            with قفل:
                منافذ_مفتوحة.append((منفذ, خدمة))
                ar(f"  {لون.أخضر}[مفتوح]{لون.إعادة}  المنفذ {لون.عريض}{منفذ}{لون.إعادة}  —  {لون.أصفر}{خدمة}{لون.إعادة}")
    except Exception:
        pass

# ── المسح السريع (منافذ شائعة) ───────────────
def مسح_سريع(هدف, مهلة=1):
    ar(f"\n{لون.أزرق}[*] وضع المسح السريع — المنافذ الشائعة فقط{لون.إعادة}\n")
    خيوط = []
    for منفذ in منافذ_شائعة:
        خ = threading.Thread(target=فحص_منفذ, args=(هدف, منفذ, مهلة))
        خيوط.append(خ)
        خ.start()
    for خ in خيوط:
        خ.join()

# ── المسح الكامل ──────────────────────────────
def مسح_كامل(هدف, من_منفذ, إلى_منفذ, مهلة=0.5):
    ar(f"\n{لون.أزرق}[*] وضع المسح الكامل — من {من_منفذ} إلى {إلى_منفذ}{لون.إعادة}\n")
    خيوط = []
    for منفذ in range(من_منفذ, إلى_منفذ + 1):
        خ = threading.Thread(target=فحص_منفذ, args=(هدف, منفذ, مهلة))
        خيوط.append(خ)
        خ.start()
        # تجنب إنشاء عدد كبير جداً من الخيوط
        if len(خيوط) >= 200:
            for t in خيوط:
                t.join()
            خيوط = []
    for خ in خيوط:
        خ.join()

# ── حل اسم النطاق ─────────────────────────────
def حل_هدف(هدف):
    try:
        ip = socket.gethostbyname(هدف)
        return ip
    except socket.gaierror:
        ar(f"{لون.أحمر}[!] تعذّر حل النطاق: {هدف}{لون.إعادة}")
        sys.exit(1)

# ── القائمة الرئيسية ──────────────────────────
def main():
    banner()

    ar(f"{لون.أصفر}أدخل الهدف (IP أو نطاق):{لون.إعادة} ", end='')
    هدف = input().strip()
    if not هدف:
        ar(f"{لون.أحمر}[!] لم تدخل هدفاً.{لون.إعادة}")
        sys.exit(1)

    ip = حل_هدف(هدف)
    ar(f"\n{لون.سماوي}[✓] الهدف: {هدف}  →  IP: {ip}{لون.إعادة}")
    ar(f"{لون.سماوي}[✓] وقت البدء: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}{لون.إعادة}\n")

    ar(f"""{لون.عريض}اختر وضع المسح:
  {لون.أخضر}1{لون.إعادة} — مسح سريع (المنافذ الشائعة فقط)
  {لون.أخضر}2{لون.إعادة} — مسح مخصص (حدد النطاق بنفسك)
  {لون.أخضر}3{لون.إعادة} — مسح كامل (1 - 65535) — بطيء!
{لون.أصفر}اختيارك:{لون.إعادة} """, end='')
    اختيار = input().strip()

    ar(f"\n{'─'*42}")

    if اختيار == '1':
        مسح_سريع(ip)

    elif اختيار == '2':
        ar(f"{لون.أصفر}من منفذ:{لون.إعادة} ", end='')
        من = int(input().strip())
        ar(f"{لون.أصفر}إلى منفذ:{لون.إعادة} ", end='')
        إلى = int(input().strip())
        مسح_كامل(ip, من, إلى)

    elif اختيار == '3':
        ar(f"{لون.أحمر}[!] تحذير: المسح الكامل قد يستغرق وقتاً طويلاً.{لون.إعادة}")
        ar(f"{لون.أصفر}هل أنت متأكد؟ (y/n):{لون.إعادة} ", end='')
        if input().strip().lower() != 'y':
            ar("تم الإلغاء.")
            sys.exit(0)
        مسح_كامل(ip, 1, 65535)

    else:
        ar(f"{لون.أحمر}[!] اختيار غير صالح.{لون.إعادة}")
        sys.exit(1)

    ar(f"\n{'─'*42}")
    if منافذ_مفتوحة:
        ar(f"\n{لون.أخضر}{لون.عريض}[✓] المنافذ المفتوحة ({len(منافذ_مفتوحة)}):{لون.إعادة}")
        for منفذ, خدمة in sorted(منافذ_مفتوحة):
            ar(f"    ► {منفذ}/TCP — {خدمة}")
    else:
        ar(f"\n{لون.أحمر}[✗] لم يُعثر على منافذ مفتوحة.{لون.إعادة}")

    ar(f"\n{لون.سماوي}[✓] انتهى المسح: {datetime.now().strftime('%H:%M:%S')}{لون.إعادة}\n")

if __name__ == '__main__':
    main()
